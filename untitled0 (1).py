from sklearn.model_selection import train_test_split

# Define features (X_df1) and target (y_df1) for df1
X_df1 = df1[['work_year', 'remote_ratio', 'company_size_encoded']]
y_df1 = df1['salary_in_usd']

# Split data into training and testing sets
