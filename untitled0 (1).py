"""### Ensemble Learning with K-Fold Cross-Validation for `df1`

We will now evaluate the same set of regression models (Linear Regression, Decision Tree, Random Forest, Gradient Boosting, SVR, and K-Neighbors Regressor) on `df1` using K-Fold Cross-Validation, assessing their performance with Root Mean Squared Error (RMSE).
"""

from sklearn.linear_model import LinearRegression
from sklearn.tree import DecisionTreeRegressor
from sklearn.ensemble import RandomForestRegressor, GradientBoostingRegressor
from sklearn.svm import SVR
from sklearn.neighbors import KNeighborsRegressor
from sklearn.model_selection import KFold, cross_val_score
from sklearn.metrics import mean_squared_error
import numpy as np

# Initialize models
models_df1 = {
    'Linear Regression': LinearRegression(),
    'Decision Tree': DecisionTreeRegressor(random_state=42),
    'Random Forest': RandomForestRegressor(random_state=42),
    'Gradient Boosting': GradientBoostingRegressor(random_state=42),
    'SVR': SVR(),
    'K-Neighbors': KNeighborsRegressor()
}

# Set up K-Fold Cross-Validation
kf_df1 = KFold(n_splits=5, shuffle=True, random_state=42)

results_df1 = {}

print('Performing K-Fold Cross-Validation for each model on df1:')

for name, model in models_df1.items():
    # Use negative mean squared error as scoring for cross_val_score
    # We'll take the square root to get RMSE
    scores_df1 = cross_val_score(model, X_train_df1, y_train_df1, cv=kf_df1, scoring='neg_mean_squared_error', n_jobs=-1)
    rmse_scores_df1 = np.sqrt(-scores_df1)

    results_df1[name] = {
        'RMSE_mean': rmse_scores_df1.mean(),
        'RMSE_std': rmse_scores_df1.std()
    }
    print(f'{name}: Mean RMSE = {results_df1[name]['RMSE_mean']:.2f}, Std RMSE = {results_df1[name]['RMSE_std']:.2f}')

# Display results in a DataFrame for easier comparison
results_df1_df = pd.DataFrame(results_df1).T
results_df1_df = results_df1_df.sort_values(by='RMSE_mean')

print('\nComparison of Model Performance on df1 (RMSE):')
display(results_df1_df)

